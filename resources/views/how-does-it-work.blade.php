Frequently Asked Questions:
<br>
1. Customers
1.A - How To Use Fast Foodie?
You can use the Fast Foodie website to request a delivery from certain merchants in your city. Once you've located the restaurant or store you'd like to order from, add the items you want to your cart, input your payment and contact info, and hit "Purchase Now".
If the merchant’s menu or catalog isn’t available in the Fast Foodie website, you’ll see the option to “Add Your Items” after choosing that merchant, which will allow you to create a custom order. Just make sure your instructions are as specific as possible so your Fast Foodie courier will know exactly what to get. For example, “One bag of potato chips, please.”

<br>
<br>
2. Independent Contractors
<br>
2.A – Requirements To Work For Fast Foodie
You must be of legal age to drive (18 years of age). Must have an iPhone or Android (smartphone), and volunteer your social security number.
<br>
2.B – Vehicle Requirements To Drive For Fast Foodie
You can use any car to deliver. Must have a valid driver’s license (government registered), car insurance, and a clean driving record.
<br>
2.C – What’s Your Job As A Fast Foodie?
You’ll be delivering food and other items from local merchants to customers.
<br>
2.D - Are Different Types Of Vehicle Allowed To Do Delivers With ?
You can use motorcycles, scooters, bikes, or even walk in some markets.
<br>
2.E - How Much Do I Make As A Fast Foodie Driver?																		       Fast Foodie pays a flat amount per delivery and you always get 100% of the tips!
<br>
2.F - When Do I Get Paid ?
You get paid on a weekly basis for all deliveries completed between Monday - Sunday of the previous week (ending Sunday at midnight ET). Payments are transferred at that time directly to your bank account through Direct Deposit. It usually takes 2-3 days to show up in your bank account, so payments will appear by Wednesday night.
<br>
2.G - Tax Information
Since Fast Foodies Couriers are Independent Contractors, we do not withhold any taxes from your pay. Fast Foodie Couriers are responsible for their own tax obligations. Fast Foodie is not able to provide any tax advice, and you should seek the assistance of a tax professional if you are unsure of how to report your income.

In the United States, all Fast Foodie Couriers that earn more than $600 within a calendar year will receive a 1099-MISC form to report the income you made with Fast Foodie.
